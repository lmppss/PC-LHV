import streamlit as st
import numpy as np
import pandas as pd
import joblib
import datetime
import pytz
import plotly.express as px
import os

# Cargar el modelo .pkl
modelo = joblib.load("PC_0.8722_12.04.pkl")

# Archivo temporal para guardar predicciones
historial_path = "historial_predicciones.csv"
if not os.path.exists(historial_path):
    pd.DataFrame(columns=["FechaHora", "Cenizas", "PC"]).to_csv(historial_path, index=False)

# T칤tulo de la app
st.title("游댠 Predicci칩n del Poder Calor칤fico del Carb칩n")
st.markdown("Ingrese los datos manualmente o pegue una fila completa separada por **coma, espacio o tabulaci칩n**.")

# Opci칩n de entrada r치pida
st.subheader("游늶 Entrada r치pida (una l칤nea completa)")
entrada_linea = st.text_input("Pegue aqu칤 una fila completa con los 11 valores en orden:")

# Bot칩n para activar la entrada manual
mostrar_entrada_manual = st.button("游닇 Mostrar entrada manual")

# Si se presiona el bot칩n, se muestran los campos de entrada manual
if mostrar_entrada_manual:
    cenizas_bs = st.number_input("Cenizas (BS) (%)", min_value=0.0)
    sio2 = st.number_input("SiO2 ash (%)", min_value=0.0)
    al2o3 = st.number_input("Al2O3 ash (%)", min_value=0.0)
    fe2o3 = st.number_input("Fe2O3 ash (%)", min_value=0.0)
    cao = st.number_input("CaO ash (%)", min_value=0.0)
    mgo = st.number_input("MgO ash (%)", min_value=0.0)
    so3 = st.number_input("SO3 ash (%)", min_value=0.0)
    na2o = st.number_input("Na2O ash (%)", min_value=0.0)
    k2o = st.number_input("K2O ash (%)", min_value=0.0)
    s_carbon = st.number_input("S carb칩n (%)", min_value=0.0)
    cl_carbon = st.number_input("Cl carb칩n (%)", min_value=0.0)

# Bot칩n de predicci칩n
if st.button("游댩 Predecir Poder Calor칤fico"):
    if entrada_linea:
        # Detectar separador
        if "," in entrada_linea:
            sep = ","
        elif "\t" in entrada_linea:
            sep = "\t"
        else:
            sep = " "
        try:
            valores = list(map(float, entrada_linea.strip().split(sep)))
            if len(valores) != 11:
                st.error("丘멆잺 Debe ingresar exactamente 11 valores.")
                st.stop()
        except:
            st.error("丘멆잺 Error en el formato de la l칤nea pegada.")
            st.stop()
    else:
        valores = [cenizas_bs, sio2, al2o3, fe2o3, cao, mgo, so3, na2o, k2o, s_carbon, cl_carbon]

    valores_np = np.array(valores).reshape(1, -1)
    pc_predicho = modelo.predict(valores_np)[0]
    pc_entero = int(round(pc_predicho))

    # Mostrar resultado
    st.success(f"游댠 Poder Calor칤fico Predicho: **{pc_entero} kcal/kg**")

    # Guardar en historial
    nuevo = pd.DataFrame([{
        "FechaHora": datetime.datetime.now(pytz.timezone('America/Lima')).strftime('%Y-%m-%d %H:%M:%S'),  # Hora de Per칰, formato string
        "Cenizas": valores[0],
        "PC": pc_entero
    }])
    historial = pd.read_csv(historial_path)
    historial = pd.concat([historial, nuevo], ignore_index=True).tail(20)
    historial.to_csv(historial_path, index=False)

    # Filtrar los datos de los 칰ltimos 3 d칤as
    fecha_3_dias_atras = datetime.datetime.now(pytz.timezone('America/Lima')) - datetime.timedelta(days=3)

    # Convertir 'FechaHora' a tipo datetime (sin modificar el formato, solo convertir la zona horaria)
    historial["FechaHora"] = pd.to_datetime(historial["FechaHora"], errors='coerce')
    historial["FechaHora"] = historial["FechaHora"].dt.tz_localize('UTC').dt.tz_convert('America/Lima')  # Convertir a la zona horaria de Per칰

    # Asegurarse de que 'fecha_3_dias_atras' tambi칠n est칠 en formato datetime
    fecha_3_dias_atras = pd.to_datetime(fecha_3_dias_atras)

    # Filtrar los datos de los 칰ltimos 3 d칤as
    historial_filtrado = historial[historial["FechaHora"] >= fecha_3_dias_atras] if not historial.empty else historial

    # Mostrar gr치fico
    st.subheader("游늳 Historial de Predicciones")
    fig = px.scatter(historial_filtrado, x="FechaHora", y="PC",
                     size="Cenizas", color="Cenizas",
                     hover_data=["Cenizas", "PC"],
                     title="Predicciones de Poder Calor칤fico vs Cenizas",
                     labels={"PC": "Poder Calor칤fico (kcal/kg)", "FechaHora": "Hora"},
                     template="plotly_dark")

    fig.update_traces(mode="markers+lines")
    st.plotly_chart(fig, use_container_width=True)

    # Mostrar historial sin opci칩n de eliminar
    st.subheader("游늵 Historial de Predicciones")
    st.dataframe(historial_filtrado, use_container_width=True)