# Cargar el modelo .pkl
modelo = joblib.load("PC_0.8722_12.04.pkl")  # Aseg칰rate de que este archivo est칠 en tu repositorio

# Inputs del usuario
st.header("游닌 Ingresar datos del an치lisis qu칤mico")

